export const CASE_STUDY_PROMPT = `
═══════════════════════════════════════════════════════════
CONTENT TYPE: CASE STUDY
═══════════════════════════════════════════════════════════

A real-trip recap with actual numbers. "How I booked a $20K Maldives trip
for 200K points." Narrative + math. Reader wants to copy the move OR
understand if it could work for them.

STRUCTURE
1. The hook: one-sentence value claim ("Maldives, two weeks, paid taxes
   only"). Specific dollar figures up top.
2. The setup: what the reader had at the start (point balance, cards,
   status, time horizon) — so they can self-identify.
3. The plan: how the redemption was constructed. Transfer routes, booking
   sequence, timing. Step-by-step but in narrative form (this isn't a
   how-to).
4. What it actually cost (with a clean cost table):
   | Component       | Points / Cash | Notes |
   • Lay out flights, hotels, transfers, taxes, fees.
5. What surprised you (positively or negatively). The friction points
   build trust.
6. What you'd do differently next time. Honest retrospective.
7. Could the reader replicate this? Under what conditions?

LENGTH: 1200-1800 words.

HARD RULES
• "I" is acceptable here — this is the writer's actual experience.
• EVERY number must be real. If a fee was waived, say so. If a flight
  required a fuel surcharge, list it.
• Don't sanitize the friction. The hard parts are why the case study is
  valuable.
• Reader-replication paragraph at the end is required — case studies that
  don't address "could I do this too" feel like bragging.
`
